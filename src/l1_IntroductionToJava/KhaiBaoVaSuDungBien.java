package l1_IntroductionToJava;

public class KhaiBaoVaSuDungBien {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        char c = 'a';
        String s = "Ha Noi";

        System.out.println("Gia tri cua bien i: " + i);
        System.out.println("Gia tri cua bien f: " + f);
        System.out.println("Gia tri cua bien d: " + d);
        System.out.println("Gia tri cua bien b: " + b);
        System.out.println("Gia tri cua bien c: " + c);
        System.out.println("Gia tri cua bien s: " + s);
    }
}
